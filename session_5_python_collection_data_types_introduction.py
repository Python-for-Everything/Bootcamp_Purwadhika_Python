# Collection Data Types Introduction

# Tipe Data List
listContoh = ['hello', 1, 1, 3, True]
print(listContoh, type(listContoh))  # Output: ['hello', 1, 1, 3, True] <class 'list'>

# Tipe Data Tuple
tupleContoh = ('hello', 1, 1, 3, True)
print(tupleContoh, type(tupleContoh))  # Output: ('hello', 1, 1, 3, True) <class 'tuple'>

# Tipe Data Set
setContoh = {'hello', 1, 1, 3, True}
print(setContoh, type(setContoh))  # Output: {1, 3, 'hello'} <class 'set'>

# Tipe Data Dictionary
dictContoh = {'nama': 'Ricky', 'usia': 25}
print(dictContoh, type(dictContoh))  # Output: {'nama': 'Ricky', 'usia': 25} <class 'dict'>
