# Definisi fungsi dengan input dan output
def tambah(angka1, angka2):
    # Mengembalikan hasil penjumlahan angka1 dan angka2
    return angka1 + angka2

# Memanggil fungsi dan menyimpan hasilnya dalam variabel
hasil1 = tambah(5, 4)
print(hasil1)  # Output: 9

# Memanggil fungsi dan langsung mencetak hasilnya
print(tambah(10, 2))  # Output: 12
